echo "  1. –ó–∞–≥—Ä—É–∑–∏—Ç–µ –Ω–æ–≤—ã–π –ø—Ä–æ–µ–∫—Ç –Ω–∞ —Å–µ—Ä–≤–µ—Ä"
echo "  2. –°–æ–∑–¥–∞–π—Ç–µ .env —Ñ–∞–π–ª –∏–∑ env.example"
echo "  3. –ó–∞–ø—É—Å—Ç–∏—Ç–µ: docker-compose up --build -d"

